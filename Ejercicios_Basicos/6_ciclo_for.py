"""-----|Ciclo for|-----------
* - "for" itera un elemento dentro de un objeto
* - range() crea un objeto iterable que contiene
*   números desde un inicio (inclusive) hasta un final
*   (excuyente)"""


for i in range(3):
    print("hola")

num = int(input("Ingresa un número: "))

for i in range(num):
    print("Hola")
